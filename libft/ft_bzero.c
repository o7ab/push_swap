#include "libft.h"

//#include <stdio.h>

void	ft_bzero(void *s, size_t n)
{
	size_t	i;

	i = 0;
	while (n > 0)
	{
		((unsigned char *)s)[i] = 0;
		n--;
		i++;
	}
}
